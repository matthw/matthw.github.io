def emulate(ctx, pc):
    while pc:
        opcode = ctx.getConcreteMemoryAreaValue(pc, 16)
        instruction = Instruction(pc, opcode)
        instruct_len = instruction.getOpcode()
        addr = instruction.getAddress()
        ctx.processing(instruction)

        #print(instruction)
        if addr in JUMP_RAX:
            jmp_target = JUMP_RAX[addr]

            rax_sym = ctx.getSymbolicRegister(ctx.registers.rax)

            if rax_sym is not None:
                # solve jump
                rax_ast = rax_sym.getAst()
                target_ast = ctx.getAstContext().bv(jmp_target, 64)
                constraint = ctx.getAstContext().equal(rax_ast, target_ast)

                model = ctx.getModel(constraint)
                if model:
                    for var_id, sol in model.items():
                        sym_var = ctx.getSymbolicVariable(var_id)
                        # sym_var.getOrigin() == the memory address it was symbolized from
                        origin = sym_var.getOrigin()    # get index
                        index = origin - TABLE_BASE
                        print("TABLE[%d] = 0x%x"%(index, sol.getValue()))

            # force flow
            ctx.setConcreteRegisterValue(ctx.registers.rax, jmp_target)
            ctx.setConcreteRegisterValue(ctx.registers.rip, jmp_target)

            #break


        if instruction.getType() == OPCODE.X86.RET:
            break

        pc = ctx.getConcreteRegisterValue(ctx.registers.rip)




def main():
    ctx = TritonContext(ARCH.X86_64)
    ctx.setMode(MODE.ALIGNED_MEMORY, True)
    #ctx.setMode(MODE.ONLY_ON_SYMBOLIZED, True)
    ctx.setSolver(SOLVER.BITWUZLA)


    # stack crap
    #STACK_BASE = 0x9ff0000
    #STACK_SIZE = 0x10000
    ctx.setConcreteRegisterValue(ctx.registers.rbp, 0x9ffffff0)
    ctx.setConcreteRegisterValue(ctx.registers.rsp, 0x9ffffff0)

    #for x in range(STACK_SIZE):
    #    ctx.setConcreteMemoryValue(MemoryAccess(STACK_BASE + x, CPUSIZE.BYTE), 0)

    # load func code
    ctx.setConcreteMemoryAreaValue(FUNC_BASE, FUNC_CODE)

    # load constants
    ctx.setConcreteMemoryAreaValue(CONSTANT_BASE, CONSTANTS)

    # symbolize memory
    for x in range(128):
        ctx.symbolizeMemory(MemoryAccess(TABLE_BASE + x, CPUSIZE.BYTE))


    emulate(ctx, FUNC_BASE)
    


if __name__ == "__main__":
    main()
